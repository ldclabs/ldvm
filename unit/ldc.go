package unit

// Denominations of value
const (
	NanoLDC  uint64 = 1
	MicroLDC uint64 = 1000 * NanoLDC
	MilliLDC uint64 = 1000 * MicroLDC
	LDC      uint64 = 1000 * MilliLDC
	KiloLDC  uint64 = 1000 * LDC
	MegaLDC  uint64 = 1000 * KiloLDC
)
